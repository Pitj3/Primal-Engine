#ifndef accessflags_h__
#define accessflags_h__

#include <cstdint>

using AccessFlags = uint32_t;

enum EAccessFlagBits
{
	ACCESS_INDIRECT_COMMAND_READ			= 0x00000001,
	ACCESS_INDEX_READ						= 0x00000002,
	ACCESS_VERTEX_ATTRIBUTE_READ			= 0x00000004,
	ACCESS_UNIFORM_READ						= 0x00000008,
	ACCESS_INPUT_ATTACHMENT_READ			= 0x00000010,
	ACCESS_SHADER_READ						= 0x00000020,
	ACCESS_SHADER_WRITE						= 0x00000040,
	ACCESS_COLOR_ATTACHMENT_READ			= 0x00000080,
	ACCESS_COLOR_ATTACHMENT_WRITE			= 0x00000100,
	ACCESS_DEPTH_STENCIL_ATTACHMENT_READ	= 0x00000200,
	ACCESS_DEPTH_STENCIL_ATTACHMENT_WRITE	= 0x00000400,
	ACCESS_TRANSFER_READ					= 0x00000800,
	ACCESS_TRANSFER_WRITE					= 0x00001000,
	ACCESS_HOST_READ						= 0x00002000,
	ACCESS_HOST_WRITE						= 0x00004000,
	ACCESS_MEMORY_READ						= 0x00008000,
	ACCESS_MEMORY_WRITE						= 0x00010000,
};

#endif // accessflags_h__
